// Flutter imports:
import 'package:flutter/material.dart';

// Package imports:
import 'package:google_fonts/google_fonts.dart';

TextStyle textWhite({
  double? size,
  FontWeight? fontWeight,
}) {
  return GoogleFonts.lato(
    fontSize: size,
    fontWeight: fontWeight,
    color: Colors.white,
  );
}
